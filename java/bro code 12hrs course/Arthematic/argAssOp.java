public class argAssOp {
    public static void main(String[] args) {

        // Augmented Assigrnment Operation

        int x = 10;
        int y = 3;

        x = x + y;
        System.out.println(x);  // 13

         x += y;
        System.out.println(x);  // 16

        x -= y;
        System.out.println(x);  //  13

         x *= y;
        System.out.println(x);  // 39

    }
}
